age <- c(23, 23, 27, 27, 39, 41, 47, 49, 50, 52, 54, 54, 56, 57, 58, 58, 60, 61)
body_fat <- c(9.5, 26.5, 7.8, 17.8, 31.4, 25.9, 27.4, 25.2, 31.1, 34.0, 
              29.3, 27.5, 25.0, 23.0, 31.1, 32.9, 41.2, 35.7)
age_mean <- mean(age)
age_median <- median(age)
age_sd <- sd(age)
fat_mean <- mean(body_fat)
fat_median <- median(body_fat)
fat_sd <- sd(body_fat)
print(paste("Age - Mean:", age_mean, " Median:", age_median, " SD:", age_sd))
print(paste("Body Fat - Mean:", fat_mean, " Median:", fat_median, " SD:", fat_sd))
par(mfrow = c(1, 2))  
boxplot(age, main = "Boxplot of Age", col = "lightblue")
boxplot(body_fat, main = "Boxplot of Body Fat (%)", col = "lightgreen")
par(mfrow = c(1, 2))  
plot(age, body_fat, main = "Scatter Plot: Age vs Body Fat", 
     xlab = "Age", ylab = "Body Fat (%)", pch = 19, col = "blue")
qqnorm(body_fat, main = "Q-Q Plot of Body Fat")
qqline(body_fat, col = "red")  
